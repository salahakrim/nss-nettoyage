            <p>Avant de pouvoir nettoyer l’intérieur de votre four refroidi, vous aurez besoin de retirer vos grilles et les placer de côté. Cela vous donne une opportunité de les faire tremper dans de l’eau savonneuse afin de retirer les graisses calcinées et l’encrassement.</p>

            <ol>
              <li>Retirer les grilles du four. Elles devraient glisser facilement.</li>
              <li>Placez vos grilles de four dans un grand évier. Cela peut être un grand bassin, un évier utilitaire ou même votre baignoire. Si vous faites tremper vos grilles dans votre salle de bain, placez des vieilles serviettes éponge dans le fond de la baignoire afin d’éviter les rayures.</li>
              <li>Remplissez votre bassin, évier ou baignoire avec suffisamment d'eau chaude pour couvrir les grilles.</li>
              <li>Versez ½ tasse de liquide vaisselle doux ou de ¾ à une tasse de détergent à lessive.</li>
              <li>Laissez les grilles tremper pendant au moins 2 heures. Les grilles très sales peuvent être laissées à tremper la nuit entière.</li>
              <li>Une fois que les grilles ont fini de tremper, utilisez votre brosse à nettoyer et votre torchon pour les frotter et les essuyer.</li>
              <li>Rincez vos grilles de four complètement.</li>
              <li>Séchez vos grilles à l’aide de torchons ou chiffons doux. Il est important de sécher complètement les grilles afin d’éviter la rouille et la corrosion.</li>
              <li>Une fois que les grilles sont sèches et que le four est propre, replacez-les dans votre four.</li>
            </ol>

